function trackers=configTrackers
        
    % configTrackers for loops
    %具体参数手动填写
    set1=[1 2 3 4];
    set2=[1 2 3 4] ;
    set3=[1 2 3 4];
    set4=[1 2 3 4];  
    tracker_name='AutoTrack';
    num_loops=length(set4)*length(set1)*length(set2)*length(set3);
    trackers_loop=cell(1,num_loops);
     for i=1:length(set1)
        for j=1:length(set2)
            for k=1:length(set3)
                for p=1:length(set4)
                        Name=[tracker_name,num2str(set1(i)),'_',num2str(set2(j)),'_',num2str(set3(k)),'_',num2str(set4(p))];
                        Paper=[tracker_name,num2str(set1(i)),'\_',num2str(set2(j)),'\_',num2str(set3(k)),'\_',num2str(set4(p))];
                        trackers_loop{length(set3)*length(set2)*length(set4)*(i-1)+length(set4)*length(set3)*(j-1)+length(set4)*(k-1)+p}=struct('name',Name,'namePaper',Paper);
                end
            end
        end
     end
     % configTrackers for loops
     % 每一行为一组参数（手动填写）
     group=[1 2 3 4;2 3 4 1;4 1 2 3;5 6 7 8];
     tracker_name='AutoTrack';
     trackers_group=cell(1,size(group,1));
     for l=1:length(trackers_group)
        Name=[tracker_name,num2str(group(l,1)),'_',num2str(group(l,2)),'_',num2str(group(l,3)),'_',num2str(group(l,4))];
        Paper=[tracker_name,num2str(group(l,1)),'\_',num2str(group(l,2)),'\_',num2str(group(l,3)),'\_',num2str(group(l,4))];
        trackers_group{l}=struct('name',Name,'namePaper',Paper);
     end
     % config Trackers for all method
    trackers_all = {
    struct('name','KCF_LinearHog','namePaper','DCF'),...
    struct('name','KCF_GaussHog','namePaper','KCF'),...
    struct('name','DSST','namePaper','DSST'),...
    struct('name','CoKCF','namePaper','CoKCF'),...
    struct('name','BACF','namePaper','BACF'),...
    struct('name','SAMF','namePaper','SAMF'),...
    struct('name','SAMF_CA','namePaper','SAMF\_CA'),...
    struct('name','Staple','namePaper','Staple'),...
    struct('name','Staple_CA','namePaper','Staple\_CA'),...
    struct('name','SRDCF','namePaper','SRDCF'),...
    struct('name','SRDCFdecon','namePaper','SRDCFdecon'),...
    struct('name','CF2','namePaper','CF2'),...
    struct('name','MCCT','namePaper','MCCT'),...
    struct('name','MCCT_H','namePaper','MCCT\_H'),...
    struct('name','CCOT','namePaper','CCOT'),...
    struct('name','CSRDCF','namePaper','CSRDCF'),...
    struct('name','STRCF','namePaper','STRCF'),...
    struct('name','DeepSTRCF','namePaper','DeepSTRCF'),...
    struct('name','ECO_gpu','namePaper','ECO\_gpu'),...
    struct('name','ECO_HC','namePaper','ECO\_HC'),...
    struct('name','TADT','namePaper','TADT'),...
    struct('name','IBCCF','namePaper','IBCCF'),...
    struct('name','UDT','namePaper','UDT'),...
    struct('name','fDSST','namePaper','fDSST'),...
    struct('name','KCC','namePaper','KCC'),...
    struct('name','UDTplus','namePaper','UDT+'),...
    struct('name','MCPF','namePaper','MCPF'),...
};

